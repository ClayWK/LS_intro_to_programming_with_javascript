// What is exception handling and what problem does it solve?

// Exception handling allows your program to anticipate areas where interaction with the real world
// could cause an error to occur, and allows you to plan in advance for how to handle that error.
// The handling could be as simple as logging to the console that an error occured, but a key
// problem being solved is that when the program contains exception handling, when an error is 
// encountered that throws an exception that is handled, the program will continue executing the
// rest of the code, rather than halting execution of the program when the error is raised.  